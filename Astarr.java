    private void aStar(int[][] graph, int[][] bandwidth, int[][] delay, double[][] reliability, int src) {
        int[] dist = new int[V];
        int[] bw = new int[V];
        int[] delayArr = new int[V];
        double[] reliabilityArr = new double[V];
        int delayThreshold = 40;
        double reliabilityThreshold = 0.70;

        PriorityQueue<Node> priorityQueue = new PriorityQueue<>();
        Set<Integer> visited = new HashSet<>();

        for (int i = 0; i < V; i++) {
            dist[i] = Integer.MAX_VALUE;
            bw[i] = 0;
            delayArr[i] = 0;
            reliabilityArr[i] = 0.0;
        }

        dist[src] = 0;
        bw[src] = 5;
        priorityQueue.add(new Node(src, 0, 0, 0, 0.0));

        while (!priorityQueue.isEmpty()) {
            Node currentNode = priorityQueue.poll();
            int u = currentNode.vertex;

            if (visited.contains(u)) {
                continue;
            }

            visited.add(u);

            for (int v = 0; v < V; v++) {
                if (graph[u][v] != 0 && !visited.contains(v) &&
                        delayArr[u] + delay[u][v] < delayThreshold &&
                        reliability[u][v] >= reliabilityThreshold) {

                    int newDistance = currentNode.distance + graph[u][v];
                    int newBandwidth = currentNode.bandwidth + bandwidth[u][v];
                    int newDelay = currentNode.delay + delay[u][v];
                    double newReliability = currentNode.reliability + reliability[u][v];

                    if (newDistance < dist[v]) {
                        dist[v] = newDistance;
                        bw[v] = newBandwidth;
                        delayArr[v] = newDelay;
                        reliabilityArr[v] = newReliability;
                        priorityQueue.add(new Node(v, newDistance, newBandwidth, newDelay, newReliability));
                    }
                }
            }
        }

        printSolution(dist, bw, delayArr, reliabilityArr);
    }

